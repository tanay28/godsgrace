        function reset_interval() {
            if (timer != 0) {
              clearInterval(timer);
              timer = 0;
              timer = setInterval("auto_logout()", 1800000);
            }
        }
        function auto_logout() {
            window.location = "<?php echo base_url('Admindashboard/logout')?>";
        }
